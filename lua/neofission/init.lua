--- @param config neofissionConfig?
neofission.setup = function(config)
  neofission.config = vim.tbl_extend("force", neofission.config, config or {})
end

-- load function
neofission.load = function()
  if vim.version().minor < 8 then
    vim.notify_once("neofission.nvim: nvim 0.8 or higher is needed")
    return
  end

  -- reset colors
  if vim.g.colors_name then
    vim.cmd.hi("clear")
  end

  vim.g.colors_name = "neofission"
  vim.o.termguicolors = true

  local groups = get_groups()

  -- add highlights
  for group, settings in pairs(groups) do
    vim.api.nvim_set_hl(0, group, settings)
  end
end

return neofission
